"""Headless "pure worker": plan a hard task on the heavy model, then drive the
browser autonomously to gather the data and auto-write an executive markdown
report.

Two deterministic phases, no human in the loop:

1. PLAN  -- the ``planner`` roster role (heavy model ``gemma-4-31b-it``) turns the
   objective into a phased, browser-aware execution plan.
2. EXECUTE -- an autonomous ``ChatAgent`` with the browser + write_file tools
   follows the plan, drives Chrome heavily, and writes the report.

Entry points: ``run_worker(task)`` (async) and ``main()`` (CLI /
``supporter-worker``).
"""

from __future__ import annotations

import argparse
import asyncio
import os
import re
import sys
from collections.abc import Callable
from datetime import datetime
from pathlib import Path
from typing import Any

from .agent import ChatAgent
from .config import config
from .llm.types import GenOptions
from .logger import logger
from .pool import get_provider
from .tools import file_ops
from .tools.browser import guardrails
from .tools.browser.session import close_session
from .tools.catalog import build_tool_catalog, select_tools

_EXECUTOR_TOOLS = ("read_file", "write_file", "browse", "web_search")
_MIN_REPORT_BYTES = 200
_DEFAULT_MAX_TURNS = 8

EXECUTOR_SYSTEM_INSTRUCTION = (
    "You are an autonomous browser worker. You execute a given objective "
    "end-to-end with NO human in the loop and you NEVER ask questions.\n\n"
    "Your primary capability is a real web browser, driven via the `browse` "
    "tool -- use it heavily and smartly:\n"
    "- browse('navigate', url=...) opens a page.\n"
    "- browse('read', url=...) reads a page as clean article markdown with "
    "in-content links -- this is your main way to ABSORB a page.\n"
    "- browse('links') lists in-content links so you know what to open next.\n"
    "- browse('snapshot') shows interactive elements for DRIVING the page; "
    "browse('click', ...) / browse('scroll', ...) / browse('newtab', ...) act "
    "on it. Paginate or scroll until you have the required item count.\n\n"
    "Work in two beats: (1) gather ALL requested data by actually visiting and "
    "reading the source(s) -- never guess or fabricate, only report what you "
    "read; (2) write ONE complete executive markdown report to the exact path "
    "you are given, using write_file. Do not stop until the report is written, "
    "and never write a stub or placeholder."
)

_REPORT_STRUCTURE = (
    "REQUIRED REPORT STRUCTURE (markdown):\n"
    "- `# <Title>` -- a clear title for the report.\n"
    "- A short metadata block: generated timestamp, the source URL(s), and the "
    "original task.\n"
    "- `## Executive Summary` -- 3-6 sentences on what was gathered and the "
    "headline takeaways.\n"
    "- `## Methodology` -- the plan that was followed and how the data was "
    "collected.\n"
    "- `## Findings` -- the items as a numbered list or table with the per-item "
    "fields (e.g. for Hacker News: rank, title, link, points, comments).\n"
    "- `## Key Themes & Insights` -- grouped analysis across the items.\n"
    "- `## Top Highlights` -- the most notable items and why.\n"
    "- A footer line noting the report was generated by `supporter-worker`.\n"
    "Make it a genuine, detailed executive report."
)


def _slugify(task: str) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", task.lower()).strip("-")[:50].strip("-")
    return slug or "task"


def _report_path(task: str, report_dir: Path | None) -> Path:
    base = report_dir or (Path(config.allowed_directories[0]) / "reports")
    stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
    return base / f"{_slugify(task)}-{stamp}.md"


def _report_ready(path: Path) -> bool:
    try:
        return path.is_file() and path.stat().st_size > _MIN_REPORT_BYTES
    except OSError:
        return False


def _approve_write(path: Path, diff: str) -> bool:
    """Headless auto-approve for write_file (sync callback)."""
    return True


async def _approve_browse(title: str, message: str) -> bool:
    """Headless auto-approve for sensitive browser actions (async callback)."""
    return True


def _install_headless_callbacks() -> Callable[[], None]:
    """Register auto-approve callbacks so writes and browser actions never block
    on interactive confirmation in headless mode.

    The confirmation callbacks are process-global, so this snapshots the prior
    callbacks and returns a restore fn -- the caller MUST call it on teardown so
    a worker run inside a live process can never permanently weaken the TUI's
    interactive confirmation gates."""
    prev_write = file_ops._CONFIRMATION_CALLBACK
    prev_browse = guardrails.browse_confirmation_callback
    file_ops.register_confirmation_callback(_approve_write)
    guardrails.register_browse_callback(confirmation=_approve_browse)

    def _restore() -> None:
        file_ops.register_confirmation_callback(prev_write)
        guardrails.register_browse_callback(confirmation=prev_browse)

    return _restore


def _planner_profile() -> dict[str, Any]:
    roster = getattr(config, "delegate_agent_roster", None) or {}
    profile = roster.get("planner")
    if profile is None:
        from .prompts import DELEGATE_AGENT_ROSTER

        profile = DELEGATE_AGENT_ROSTER["planner"]
    return profile


async def _make_plan(task: str) -> str:
    """Phase 1: ask the heavy-model planner role for an execution plan."""
    profile = _planner_profile()
    provider = get_provider(model_name=profile["model"], shared=False)
    # REST providers drop the factory system_instruction, so the persona MUST
    # ride the per-call options or the planner falls back to the default prompt.
    options = GenOptions(system_instruction=profile["persona"])
    result = await provider.generate(f"OBJECTIVE:\n{task}", options)
    plan = (getattr(result, "text", "") or "").strip()
    logger.info(f"worker: planner produced a {len(plan)}-char plan")
    return plan


def _build_executor_agent(session_id: str) -> ChatAgent:
    """Phase 2 setup: an autonomous agent with browser + write tools."""
    model = os.getenv("WORKER_EXECUTOR_MODEL") or config.gemini_model
    registry = select_tools(
        build_tool_catalog(include_bash=False), _EXECUTOR_TOOLS
    )
    # ChatAgent supplies the system_instruction per-call via GenOptions; the REST
    # factory would silently drop it here, so it is intentionally not passed.
    provider = get_provider(model_name=model, registry=registry, shared=False)
    return ChatAgent(
        provider,
        registry=registry,
        use_search=False,
        use_code_execution=False,
        system_instruction=EXECUTOR_SYSTEM_INSTRUCTION,
        session_id=session_id,
    )


def _build_executor_prompt(task: str, plan: str, report_path: Path) -> str:
    return (
        "You are running as an autonomous worker. Execute the objective "
        "end-to-end now.\n\n"
        f"OBJECTIVE:\n{task}\n\n"
        "PLAN (from the planner -- follow it):\n"
        f"{plan or '(no plan provided; use your judgment)'}\n\n"
        "EXECUTION RULES:\n"
        "- Drive the browser HEAVILY via the browse tool to actually visit and "
        "READ the source(s); paginate/scroll to reach the required item count.\n"
        "- Gather ALL requested data first; only report what you actually read.\n"
        f"- When the data is complete, write the FINAL report to this EXACT "
        f"path via write_file(path='{report_path}', content=<full markdown>).\n\n"
        f"{_REPORT_STRUCTURE}"
    )


def _continuation_prompt(report_path: Path) -> str:
    return (
        "Continue executing the plan. You have NOT yet written the final report "
        f"to {report_path}. Keep driving the browser to gather any remaining "
        "data, then write the complete executive markdown report to that exact "
        f"path via write_file(path='{report_path}', content=<full markdown>)."
    )


async def _teardown(agent: ChatAgent | None) -> None:
    try:
        await close_session(force=True)
    except Exception as exc:  # best-effort teardown
        logger.debug(f"worker: close_session error: {exc}")
    provider = getattr(agent, "provider", None)
    close = getattr(provider, "close", None)
    if close is None:
        return
    try:
        result = close()
        if hasattr(result, "__await__"):
            await result
    except Exception as exc:  # best-effort teardown
        logger.debug(f"worker: provider close error: {exc}")


async def run_worker(
    task: str,
    *,
    report_dir: Path | None = None,
    max_executor_turns: int = _DEFAULT_MAX_TURNS,
) -> Path:
    """Plan the task, drive the browser to fulfil it, and write an executive
    markdown report. Returns the report path. Raises ``RuntimeError`` if no
    report was produced within ``max_executor_turns``."""
    task = task.strip()
    if not task:
        raise ValueError("task must be a non-empty string")

    restore_callbacks = _install_headless_callbacks()
    agent: ChatAgent | None = None
    report_path = _report_path(task, report_dir)
    try:
        report_path.parent.mkdir(parents=True, exist_ok=True)
        logger.info(f"worker: task={task!r} -> report {report_path}")

        plan = await _make_plan(task)
        session_id = f"worker-{datetime.now().strftime('%Y%m%d-%H%M%S-%f')}"
        agent = _build_executor_agent(session_id)
        prompt = _build_executor_prompt(task, plan, report_path)

        for turn in range(1, max_executor_turns + 1):
            logger.info(f"worker: executor turn {turn}/{max_executor_turns}")
            try:
                await agent.execute(prompt)
            except Exception as exc:  # keep driving on a transient turn failure
                logger.warning(f"worker: executor turn {turn} errored: {exc}")
            if _report_ready(report_path):
                logger.info(f"worker: report ready after turn {turn}")
                break
            prompt = _continuation_prompt(report_path)
        else:
            logger.warning("worker: exhausted executor turns without a report")
    finally:
        await _teardown(agent)
        restore_callbacks()

    if not _report_ready(report_path):
        raise RuntimeError(f"worker did not produce a report at {report_path}")
    logger.info(f"worker: done -> {report_path}")
    return report_path


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(
        prog="supporter-worker",
        description=(
            "Autonomous worker: plan a task, drive the browser, and write an "
            "executive markdown report."
        ),
    )
    parser.add_argument(
        "task", nargs="*", help='the task, e.g. "open hackernews and get 50 news"'
    )
    parser.add_argument(
        "--report-dir",
        dest="report_dir",
        default=None,
        help="directory for the report (default: <project>/reports)",
    )
    parser.add_argument(
        "--max-turns",
        dest="max_turns",
        type=int,
        default=_DEFAULT_MAX_TURNS,
        help="max executor turns before giving up",
    )
    args = parser.parse_args(argv)

    task = " ".join(args.task).strip()
    if not task:
        sys.stderr.write('error: no task provided\nusage: supporter-worker "<task>"\n')
        return 2

    report_dir = Path(args.report_dir) if args.report_dir else None
    try:
        path = asyncio.run(
            run_worker(task, report_dir=report_dir, max_executor_turns=args.max_turns)
        )
    except Exception as exc:
        logger.error(f"worker failed: {exc}")
        sys.stderr.write(f"error: {exc}\n")
        return 1

    sys.stdout.write(f"{path}\n")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
